        <p>A história de Capitólio, em Minas Gerais, começou a ser desenhada por volta de 1800, quando surgiram, na região de Capitólio, conhecida como “Mata do Rio Piumhi”, dois portugueses, Machado de Faria e Gonçalves de Morais. O potencial das matas, praticamente intocadas, chamou a atenção dos estrangeiros que foram os primeiros exploradores da região.</p>
