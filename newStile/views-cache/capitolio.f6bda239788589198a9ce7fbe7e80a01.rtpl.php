        <p>Por volta de 1830, vieram os primeiros moradores para a região onde se desenvolveu a cidade. Trata-se de três irmãos: João Francisco, Manoel Francisco e Antônio Francisco, considerados os fundadores do povoado que originou Capitólio. A partir de então um povoado foi surgindo, nascendo assim o “Arraial dos Franciscos”, primeiro nome dado ao lugar onde está a cidade de Capitólio, em homenagem aos antigos donos das terras. Esse Arraial era conhecido, também, como “Arraial dos Cabeças”, não somente porque os Franciscos eram os principais líderes (cabeças) do lugar, como também, ao que diziam, eram homens de cabeças muito grandes. </p>
             
         <p>   Em 1893, chegou ao Arraial dos Franciscos o fazendeiro Pedro Messias da Cunha, que se instalou nas imediações do arraial, adquirindo grandes proporções de terra. Em1895, doou à comunidade um terreno para construção de uma capela, que ele mesmo ergueu em honra a São Sebastião, padroeiro da cidade. Daí em diante o arraial ficou com o nome de “Arraial de São Sebastião dos Franciscos”.
             </p>
         <p>  São Sebastião dos Franciscos foi elevado à categoria de distrito em 7 de setembro de 1923, por uma lei que criou o Distrito de Capitólio, desmembrando-o do município de Piumhi e o anexou ao de Guapé.
        </p>  
         <p>   Em 1939, o Distrito de Capitólio foi elevado à categoria de vila, Vila de Capitólio. Depois de uma tentativa fracassada, em 1943, Capitólio conseguiu sua independência político-administrativa no dia 27 de dezembro de 1948, transformando-se em Município de Capitólio.
